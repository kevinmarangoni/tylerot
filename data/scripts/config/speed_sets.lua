-- Funções auxiliares
function SpeedSets.getVocationSet(vocationId)
    return SpeedSets[vocationId]
end

function SpeedSets.getVocationName(vocationId)
    local set = SpeedSets[vocationId]
    return set and set.name or "Desconhecida"
end

function SpeedSets.getItems(vocationId)
    local set = SpeedSets[vocationId]
    return set and set.items or {}
end

function SpeedSets.hasSet(vocationId)
    local set = SpeedSets[vocationId]
    return set and #set.items > 0
end

-- Função para mapear vocações promovidas para básicas (fallback)
function SpeedSets.getBaseVocation(vocationId)
    local vocationMap = {
        [5] = 1, -- Master Sorcerer -> Sorcerer
        [6] = 2, -- Elder Druid -> Druid
        [7] = 3, -- Royal Paladin -> Paladin
        [8] = 4, -- Elite Knight -> Knight
    }
    return vocationMap[vocationId] or vocationId
end

-- Função inteligente para obter set (tenta vocação específica, senão usa a básica)
function SpeedSets.getVocationSetSmart(vocationId)
    local set = SpeedSets[vocationId]
    if set and #set.items > 0 then
        return set
    end

    -- Se não encontrar, tenta a vocação básica
    local baseVocationId = SpeedSets.getBaseVocation(vocationId)
    return SpeedSets[baseVocationId]
end

